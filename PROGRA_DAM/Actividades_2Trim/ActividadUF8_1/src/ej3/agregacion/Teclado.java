package ej3.agregacion;

public class Teclado {

	private String idioma; //qwerty
	private String tipo;  //mecanico,...
	
	
	public Teclado(String idioma, String tipo) {
		this.idioma = idioma;
		this.tipo = tipo;
	}

	public String getIdioma() {
		return idioma;
	}

	public String getTipo() {
		return tipo;
	}
	
	
	
	
}
